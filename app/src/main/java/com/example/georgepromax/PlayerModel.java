package com.example.georgepromax;

public class PlayerModel {
    String userName, password, email, phoneNumber,gameTimeBest;

    public PlayerModel(){};

    public PlayerModel(String userName, String password, String email, String phoneNumber) {
        this.userName = userName;
        this.password = password;
        this.email = email;
        this.phoneNumber = phoneNumber;
    }

    public String getGameTimeBest() { return gameTimeBest; }

    public void setGameTimeBest(String gameTimeBest) { this.gameTimeBest = gameTimeBest; }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }
}